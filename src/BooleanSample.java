public class BooleanSample {
    public static void main(String[] args) {
        boolean isRaining = true;
        if (isRaining) {
            System.out.println("It's raining. I don't want to go outside.");
        } else {
            System.out.println("It's not raining. I want to go outside.");
        }

        boolean isSunny = false;
        if (isSunny) {
            System.out.println("It's sunny. I want to go outside.");
        } else {
            System.out.println("It's not sunny. I don't want to go outside.");
        }
    }
}
